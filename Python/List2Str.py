
# Python program to convert a list 
# to string using join() function 
    
# Function to convert   
def listToString(my_List):  
    
    # initialize an empty string 
    str1 = " " 
    
    # return string   
    return (str1.join(my_List)) 
        
        
# Driver code     
s = ['Python', 'improves', 'productivity'] 
print(listToString(s))  
